#pragma once
#include <string>

namespace DigitInput {

// Чистая функция: даны текст числового поля и позиция курсора (0..text.size()),
// direction = +1 для ↑ / -1 для ↓. Возвращает true и заполняет new_text/new_cursor,
// если ввод — распознаваемое число (plain decimal / scientific / fraction).
// Возвращает false для пустой строки, невалидного ввода, куска без цифр.
//
// Правила:
//   - Редактируется цифра СЛЕВА от каретки. Если слева цифры нет (каретка в
//     самом начале / слева знак / слева точка) — берётся ближайшая справа.
//   - Значение_части += direction * 10^разряд.
//   - Знак "-" не считается разрядом.
//   - Число дробных знаков сохраняется (12.345 - 0.001 → 12.344, не 12.34400).
//   - Ведущие нули НЕ добавляются: 1000 - 100 → 900 (а не "0900").
//   - После правки каретка встаёт СПРАВА от изменённой цифры — так повторные
//     ↑/↓ продолжают крутить тот же разряд без смещения по позициям.
//
// Составные форматы:
//   - Scientific "1.5e-4": каретка слева от 'e'/'E' → редактируется мантисса,
//     справа → экспонента. Только та часть, где сейчас каретка.
//   - Fraction "8/3": каретка слева от '/' → числитель, справа → знаменатель.
//   - Обе части обрабатываются независимо, друг на друга не влияют
//     (нормализация мантиссы/дроби не выполняется — сохраняем ровно то,
//     что ввёл пользователь).
bool ComputeStep(const std::string& text, int cursor, int direction,
                 std::string& new_text, int& new_cursor);

} // namespace DigitInput
